import { useSelector } from "react-redux";
import useMovieTrailer from "../hooks/useMovieTrailer";


const VideoBackground = ({ movieId }) => {
  const trailerVideo = useSelector((store) => store.movies?.trailerVideo?.trailer[0]);

  useMovieTrailer(movieId);
 
  

  return (
    <div className="relative w-full h-[720px] overflow-hidden">
      <iframe
        className="absolute top-0 left-0 w-full h-full scale-150"
        src={`https://www.youtube.com/embed/${trailerVideo?.key}?autoplay=1&loop=1&mute=1&playlist=${trailerVideo?.key}&vq=hd1080`}
        title="YouTube video player"
        allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share"
        referrerPolicy="strict-origin-when-cross-origin"
      ></iframe>
    </div>
  );
};

export default VideoBackground;
